
public class ArabaBilgileri {
    
    private String model;
    private int yil;
    private int kapi;
    private String renk;
    private String uretimyeri;

    /**
     * @return the model
     */
    public String getModel() {
        return model;
    }

    /**
     * @param model the model to set
     */
    public void setModel(String model) {
        this.model = model;
    }

    /**
     * @return the yil
     */
    public int getYil() {
        return yil;
    }

    /**
     * @param yil the yil to set
     */
    public void setYil(int yil) {
        this.yil = yil;
    }

    /**
     * @return the kapi
     */
    public int getKapi() {
        return kapi;
    }

    /**
     * @param kapi the kapi to set
     */
    public void setKapi(int kapi) {
        this.kapi = kapi;
    }

    /**
     * @return the renk
     */
    public String getRenk() {
        return renk;
    }

    /**
     * @param renk the renk to set
     */
    public void setRenk(String renk) {
        this.renk = renk;
    }

    /**
     * @return the uretimyeri
     */
    public String getUretimyeri() {
        return uretimyeri;
    }

    /**
     * @param uretimyeri the uretimyeri to set
     */
    public void setUretimyeri(String uretimyeri) {
        this.uretimyeri = uretimyeri;
    }
}



    private void BAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAddActionPerformed
        
        LMessage.setText("");
        
        if (TFBName.getText().trim().equals("") && TFAName.getText().trim().equals("") && 
            TFPName.getText().trim().equals("") && TFNPage.getText().trim().equals(""))
        {
            LMessage.setText("Fill All The Fields!");
        }
        else
        {
           String bName = TFBName.getText();
           String aName = TFAName.getText();
           String pName = TFPName.getText();
           String nPage = TFNPage.getText();
        
            lb.addBook(bName, aName, pName, nPage);
        
            showBooks();
        
            LMessage.setText("The Book has been Added Successfully!");
        
            TFBName.setText("");
            TFAName.setText("");
            TFPName.setText("");
            TFNPage.setText(""); 
        }
        
        
        
    }//GEN-LAST:event_BAddActionPerformed

    private void BDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BDeleteActionPerformed
        
        LMessage.setText("");
        
        int selectedRow = TBookInfos.getSelectedRow();
        
        if (selectedRow == -1)
        {
            if(model.getRowCount() == 0)
            {
                
                LMessage.setText("There are no books in the table!");
                
            }
            
            LMessage.setText("If you want to delete a book you have the select a row!");
         
        }
        else
        {
            int id = (int) model.getValueAt(selectedRow, 0);
            
            lb.deleteBook(id);
            
            LMessage.setText("The Book Has Been Deleted Succesfully!");
            
            showBooks();
            
            TFBName.setText("");
            TFAName.setText("");
            TFPName.setText("");
            TFNPage.setText("");
        }
        
        
        
        
        
    }//GEN-LAST:event_BDeleteActionPerformed

    private void BUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUpdateActionPerformed
       
        String bName = TFBName.getText();
        String aName = TFAName.getText();
        String pName = TFPName.getText();
        String nPage = TFNPage.getText();
        
        int selectedRow = TBookInfos.getSelectedRow();
        
        if (selectedRow == -1)
        {
            if(model.getRowCount() == 0)
            {
                
                LMessage.setText("There are no books in the table!");
                
            }
            
            LMessage.setText("If you want to update a book you have the select a row!");
            
        }
        else
        {
           int id = (int) model.getValueAt(selectedRow, 0);
           
           lb.updateBook(id, bName, aName, pName, nPage);
           
           LMessage.setText("The Book Has Been Updated Succesfully!");
           
           showBooks();
            
           TFBName.setText("");
           TFAName.setText("");
           TFPName.setText("");
           TFNPage.setText("");
        }
        
        
    }//GEN-LAST:event_BUpdateActionPerformed


    public void showBooks()
    {
        model.setRowCount(0);
        ArrayList<BooksInfo> books = new ArrayList<BooksInfo>();

        books = lb.showBooks();
        
        if (books != null)
        {
            for (BooksInfo b : books)
            {
                Object[] add = {b.getId(),b.getBookName(),b.getAuthorName(),b.getPublisherName(),b.getNumberPage()};
                
                model.addRow(add);         
            }
            
        }
        
        
    }
    


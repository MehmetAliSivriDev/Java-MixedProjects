    private void BPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPlayActionPerformed
    
        int computerChoice = r.nextInt(3);
        
        if (computerChoice == 0)
        {
            BComputerIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/rock.png"))); 
        }
        if (computerChoice == 1)
        {
            BComputerIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/paper.png"))); 
        }
        if (computerChoice == 2)
        {
            BComputerIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/scissors.png"))); 
        }
        //**************************************************************************************************
        
        if(String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Rock") && computerChoice == 0)
        {
            LMessage.setText("Draw!");
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Rock") && computerChoice == 1)
        {
            LMessage.setText("Computer WIN!");
            computerScore++;
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Rock") && computerChoice == 2)
        {
            LMessage.setText(TFPName.getText() + " WIN!");
            playerScore++;
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Paper") && computerChoice == 0)
        {
            LMessage.setText(TFPName.getText() + " WIN!");
            playerScore++;
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Paper") && computerChoice == 1)
        {
            LMessage.setText("DRAW!");
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Paper") && computerChoice == 2)
        {
            LMessage.setText("Computer WIN!");
            computerScore++;
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Scissors") && computerChoice == 0)
        {
            LMessage.setText("Computer WIN!");
            computerScore++;
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Scissors") && computerChoice == 1)
        {
            LMessage.setText(TFPName.getText() + " WIN!");
            playerScore++;
        }
        else if (String.valueOf(CBPlayerChoice.getSelectedItem()).equals("Scissors") && computerChoice == 2)
        {
            LMessage.setText("DRAW!");
        }
        //****************************************************************************************************
        
        LPScore.setText(String.valueOf(playerScore));
        LCScore.setText(String.valueOf(computerScore));
        
        if(playerScore == 3)
        {
            JOptionPane.showMessageDialog(this, TFPName.getText() + " WIN! " + "Score : " + playerScore + " - " + computerScore);
            endGame();
        }
        if(computerScore == 3)
        {
            JOptionPane.showMessageDialog(this, "Computer WIN! " + "Score : " + computerScore + " - " + playerScore);
            endGame();
        }
        
    }//GEN-LAST:event_BPlayActionPerformed

